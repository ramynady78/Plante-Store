function validation(){
    const payNow = document.querySelector(".pay-now");
    const clientName= document.querySelector(".client-name");
    const clientNumber= document.querySelector(".client-number");
    const clientEmail= document.querySelector(".client-email");
    const clientAddres= document.querySelector(".client-addres");
    const cardNumber = document.querySelector(".card-number");
    const cardDate = document.querySelector(".card-date");
    const cardCvs = document.querySelector(".card-cvs");
    const paymentPage = document.querySelector(".payment-page")


    const clientNameRe = /[a-zA-Z]+(\s[a-zA-Z]+)/;
    const clientNumberRe = /(^(  8|\+7|\+2|\+1|0)\s?\d{3}\s?\d{3}\s?\d{2}\s?\d{2}$)/;
    const clientEmailRe = /^\w+@\w+.(com|ru|org|net|cloud)$/;

    const cardNumberRe = /^\d{4}\s?\d{4}\s?\d{4}\s?\d{4}$/;
    const cardDateRe = /^\d{2}\/\d{2}$/;
    const cardCvsRe = /^\d{3}$/;

 

    function validateForm() {
        if (clientNameRe.test(clientName.value) && clientNumberRe.test(clientNumber.value)
            && clientEmailRe.test(clientEmail.value)&& clientAddres.value.length !== 0
            && cardNumberRe.test(cardNumber.value) && cardDateRe.test(cardDate.value) 
            && cardCvsRe.test(cardCvs.value)) {
            payNow.disabled = false;
        } else {
            payNow.disabled = true;
        }
    }

    clientName.addEventListener("input" , validateForm);
    clientNumber.addEventListener("input" , validateForm);
    clientEmail.addEventListener("input" , validateForm);
    clientAddres.addEventListener("input" , validateForm);
    cardNumber.addEventListener("input" , validateForm);
    cardDate.addEventListener("input" , validateForm);
    cardCvs.addEventListener("input" , validateForm);
    payNow.addEventListener("click" ,()=> {
        alert("Спасибо за заказ, ваш заказ прибудет через 14 дней.");
        paymentPage.style.display = "none";
        document.querySelector(".card-info").style.display = "none";

    })
 

}

